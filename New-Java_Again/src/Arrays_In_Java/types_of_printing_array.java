package Arrays_In_Java;

public class types_of_printing_array {
    public static void main(String[] args) {

        int[] a={10,20,30,40,50};

        System.out.println("First : For Loop - ");

        for (int i=0;i<a.length;i++){
            System.out.print(a[i]+" ");
        }
        System.out.println();

    }
}
